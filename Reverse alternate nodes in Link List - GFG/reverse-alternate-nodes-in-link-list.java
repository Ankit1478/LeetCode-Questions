class Solution
{
 
    static Node reverse(Node head) {
        Node prev = null;
        Node curr = head;
        Node next;
        while (curr != null) {
            next = curr.next;
            curr.next = prev;
            prev = curr;
            curr = next;
        }
        return prev;
    }
    public static void rearrange(Node head)
    {
        // add your code here
       
      if (head == null || head.next == null) {
        return;
       }
       
       Node curr = head;
       Node temp = head.next;
       Node store = temp;
       
       while(curr.next!=null && temp.next!=null){
           
           curr.next = curr.next.next;
           curr = curr.next;
           
           
           temp.next = temp.next.next;
           temp = temp.next;
           
       }
       
       Node ans = reverse(store);
       curr.next = ans;
    
    }
}














